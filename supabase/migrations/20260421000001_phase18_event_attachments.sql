-- ============================================================
-- WeGoTwo · Phase 18 · events.attachments
--
-- Событию таймлайна может соответствовать больше одного документа
-- (два посадочных на один рейс — Кирилл и Марина). Раньше мы
-- хранили ровно один document_id на событие, и второй билет
-- порождал дубликат события. Теперь ingest собирает все документы
-- одного рейса в массив `attachments`.
--
-- Формат элемента: { document_id: uuid, label: text|null }.
-- `label` — имя пассажира (если Gemini извлёк) либо название
-- документа (documents.title). Рендерится как «🎫 Билет {label}».
--
-- `document_id` остаётся как «основной источник» первой аттачмент —
-- для обратной совместимости со старыми событиями; Timeline
-- предпочитает attachments, когда массив непустой.
-- ============================================================

alter table public.events
  add column if not exists attachments jsonb not null default '[]'::jsonb;

comment on column public.events.attachments is
  'Array of {document_id,label} — вложенные документы события (например, два посадочных одного рейса).';
